#define our add function
def add(addend1, addend2):
    return addend1 + addend2

def Subtract(num1, num2):
    return num1 - num2

def multiply(num1, num2):
    return num1 * num2

def division(num1, num2):
    return num1 / num2
    

# call our add function and store and store its output inside the sum variable
sum = add(5, 35)
#print the sum variable
print(sum)
print(Subtract(100, 55))
print(multiply(5, 5))
print(division(100, 25))
